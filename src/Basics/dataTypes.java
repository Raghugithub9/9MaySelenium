package Basics;

public class dataTypes {

	public static void main(String[] args) {
	
	int a=123;//numbers upto 32bit
	long l=1234567;//numbers upto 64bit
	
	float f=12.23f;//store 2 decimals upto 32bit
	double d=2345.23465d;//store more than 2 decimals upto 64bit

	//single letter or special characters
	char c= 'A';
	
	String st="Selenium Test";
	
	boolean b=true;
	
	System.out.println(a);
	System.out.println(l);
	System.out.println(f);
	System.out.println(d);
	System.out.println(c);
	System.out.println(st);
	System.out.println(b);
	
	}

}
